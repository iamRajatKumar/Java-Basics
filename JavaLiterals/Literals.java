package JavaLiterals;

public class Literals {

    public static void main(String[] args) {
        int x = 0b10011101;

        System.out.println(x);

        char c = '\u0061';
        System.out.println(c);

        double d = 134.25;
        System.out.println(d);

        String s ="Hello";
        System.out.println(s);

        Boolean b = true;
        System.out.println(b);
    }
    
}
